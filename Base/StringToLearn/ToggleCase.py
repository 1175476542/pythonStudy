# 大小写转换
str1 = "to be or not to be,That's a question"
newStr1 = str1.capitalize()# To be or not to be,that's a question
newStr2 = str1.title()# To Be Or Not To Be,That'S A Question
newStr3 = str1.upper()# TO BE OR NOT TO BE,THAT'S A QUESTION
newStr4 = str1.lower()# to be or not to be,that's a question
print(newStr1)
print(newStr2)
print(newStr3)
print(newStr4)